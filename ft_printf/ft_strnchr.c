#include "ft_printf.h"

size_t	ft_strnchr(const char *s, char c)
{
	size_t i;
	size_t n;

	if (s == NULL)
		return (0);
	i = 0;
	n = 0;
	while (s[i])
	{
		if (s[i] == c)
			n++;
		i++;
	}
	return (n);
}
